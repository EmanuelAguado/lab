from typing import Callable, Union
from functools import partial
from logging import getLogger
import urllib.request
from http.client import HTTPResponse

logger = getLogger(__name__)

from PySide6.QtCore import (
    QObject,
    Signal,
    QUrl,
)
from PySide6.QtNetwork import QNetworkAccessManager, QNetworkReply, QNetworkRequest
from PySide6.QtWidgets import QApplication
from PySide6.QtGui import QPixmap

try:
    from functools import cache
except ImportError as e:
    from functools import lru_cache
    cache = lru_cache(maxsize=None, typed=True)

@cache
def get_default_icon(link):
    pixmap = QPixmap()
    if isinstance(link, bytes):
        image_data = link
        pixmap.loadFromData(image_data)
        return pixmap
    try:
        response: HTTPResponse = urllib.request.urlopen(link)
        image_data = response.read()
        pixmap.loadFromData(image_data)
    except Exception as e:
        logger.warning(e)
    return pixmap


class WebImageDownloader(QObject):
    on_start_download = Signal(object, object)

    def __new__(cls):
        if not hasattr(cls, "instance"):
            cls.instance = super().__new__(cls)
        return cls.instance

    def __init__(self):
        if hasattr(self, "initialized"):
            return
        super().__init__()
        self.initialized = True
        self._manager = QNetworkAccessManager()
        self._manager.finished.connect(partial(self.handle_finished))
        self.on_start_download.connect(partial(self.start_download))
        self._callback_dict = dict()
        self.downloaded_files = dict()

    def start_download(self, url: Union[str, QUrl], callback: Callable):
        """Downloads a file from a URL and adds it as a key to the calback dict."""
        # logger.debug(f"Requesting url ending in {url.toString()[-5:]}")
        if url not in self._callback_dict.keys():
            self._callback_dict[url] = [callback]
            self._manager.get(QNetworkRequest(url))
        else:
            self._callback_dict[url].append(callback)

    def handle_finished(
        self, reply: QNetworkReply
    ):
        """Handles the reply from the donwload request to check how did the download do."""
        url = reply.request().url()
        data = None
        if reply.error() == QNetworkReply.NoError:
            data = reply.readAll()
        for func in self._callback_dict[url]:
            func(data)
        reply.deleteLater()
        del self._callback_dict[url]


